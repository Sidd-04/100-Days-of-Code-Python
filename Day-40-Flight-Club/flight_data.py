class FlightData:

    def __init__(self, price, departure_city_name, departure_airport_code, arrival_city_name, arrival_airport_code,
                 outbound_date, inbound_date, stop_overs=0, via_city=""):
        self.price = price
        self.departure_city_name = departure_city_name,
        self.departure_airport_code = departure_airport_code,
        self.arrival_city_name = arrival_city_name,
        self.arrival_airport_code = arrival_airport_code,
        self.outbound_date = outbound_date
        self.inbound_date = inbound_date
        self.stop_overs = stop_overs
        self.via_city = via_city









